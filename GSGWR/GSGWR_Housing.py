"""
Optimized GSGWR Estimation Code for Large-Sample Datasets (Exemplified with Housing Data)
Paper: Geographical Similarity and Geographically Weighted Regression (GSGWR): Operationalizing the Second Law of Geography in a Regression Framework by Integrating the First and Third Laws

# Repository Folder Structure
The entire repository consists of two top-level folders:

1. /GSGWR/ (this code folder)
   - `GSGWR_Housing.py`: Optimized batch‑processing and memory‑saving version of GSGWR, designed specifically for large‑sample datasets (here applied to the Housing dataset as an example).
   - `GSGWR_main.py`: Standard GSGWR script for small‑ to medium‑sized datasets (zn, Crime, HIV, Depression).
   - `Preprocess_GeoMatrix.py`: Generates the geographical spatial distance matrix.
   - `Preprocess_SimMatrix.py`: Generates the attribute similarity distance matrix.

2. /Data/ (sibling folder of /GSGWR/, stores all raw data and matrix outputs)
   - `Housing.csv`: Raw housing price dataset.
   - `Geographical_distance_matrix.csv`: Large spatial distance matrix generated by the preprocessing code.
   - `Similarity_distance_matrix.csv`: Large attribute similarity distance matrix generated by the preprocessing code.

# Key Differences Between the Two GSGWR Scripts
1. Core Model Mathematics: Both scripts use the same Gaussian kernel, leave‑one‑out cross‑validation (LOO‑CV) loss function, and local weighted regression formulation; there is no difference in model specification.
2. Optimization Design (for large‑sample data, such as this Housing dataset):
   - Numba JIT acceleration for kernel and weight calculations;
   - Batch‑by‑batch local model fitting to control RAM usage;
   - Chunked loading of large matrices to avoid memory overflow;
   - A four‑stage finer bandwidth refinement search to avoid local CV minima in dense large‑sample settings.
   *Note: The standard script `GSGWR_main.py` can load the full matrix directly for small‑ to medium‑sized datasets (N < 3000), where three rounds of coarse‑to‑fine bandwidth search suffice. For large‑sample data (e.g., N ≈ 20,000 in this example), using the standard script would result in slow computation and potential memory exhaustion, hence the need for the optimizations above.*
3. Use Case for `GSGWR_main.py`:
   Suitable for small‑ to medium‑sized datasets (N < 3000, e.g., zn, Crime, HIV, Depression). Full matrix loading is feasible; three rounds of coarse‑to‑fine bandwidth search are sufficient, and additional refinement stages would incur redundant computational cost.

# Full Reproduction Workflow for the Housing Dataset
1. First generate the two large distance matrix files
   Run the two preprocessing scripts located in `/GSGWR/`:
   - `Preprocess_GeoMatrix.py` outputs `Geographical_distance_matrix.csv`, saved to `../Data/`
   - `Preprocess_SimMatrix.py` outputs `Similarity_distance_matrix.csv`, saved to `../Data/`

2. Run this script directly – no manual filename modification is needed (pre‑configured for the Housing data)
   - Mode 1: Full four‑stage bandwidth optimization (reproduces the complete CV search process).
   - Mode 2: Fast fitting using pre‑estimated optimal bandwidths (the actual values should be determined by searching over the data or by roughly assessing the matrix ranges; the numbers here are for illustration only).

3. Output Storage
   All batch‑wise local coefficients, predicted values, residuals, and evaluation metrics will be automatically saved to the `./output_housing/` folder inside `/GSGWR/`.
"""
